   //******************************************************************************************************************
   void Ferret::Rotate(float aRotationChange)
   {
      mRotation += aRotationChange;
      mRotation = fmod(mRotation, 360.0F);
      if (mRotation < 0.0F)
      {
         mRotation += 360.0F;
      }

      mSprite->UpdateRotation(mRotation * (3.14F/180.0F)); // TODO: Make constant for degree->radians.
   }

